public class Mahasiswa {
    private int NIM;
    private String nama;
    private String prodi;
    private String fakultasDepartemen;

    public int getNIM() {
        return NIM;
    }

    public void setNIM(int NIM) {
        this.NIM = NIM;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getProdi() {
        return prodi;
    }

    public void setProdi(String prodi) {
        this.prodi = prodi;
    }

    public String getFakultasDepartemen() {
        return fakultasDepartemen;
    }

    public void setFakultasDepartemen(String fakultasDepartemen) {
        this.fakultasDepartemen = fakultasDepartemen;
    }
}