public class Penerbit {
    private int IDPenerbit;
    private String nama;
    private String alamat;
    private String email;
    private String noTelep;

    public int getIDPenerbit() {
        return IDPenerbit;
    }

    public void setIDPenerbit(int IDPenerbit) {
        this.IDPenerbit = IDPenerbit;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getNoTelep() {
        return noTelep;
    }

    public void setNoTelep(String noTelep) {
        this.noTelep = noTelep;
    }
}