public class ClientMember {
    private int idIdent;
    private String NIK;
    private int punyaIdent;
    private String jumlahDenda;

    public int getIdIdent() {
        return idIdent;
    }

    public void setIdIdent(int idIdent) {
        this.idIdent = idIdent;
    }

    public String getNIK() {
        return NIK;
    }

    public void setNIK(String NIK) {
        this.NIK = NIK;
    }

    public int getPunyaIdent() {
        return punyaIdent;
    }

    public void setPunyaIdent(int punyaIdent) {
        this.punyaIdent = punyaIdent;
    }

    public String getJumlahDenda() {
        return jumlahDenda;
    }

    public void setJumlahDenda(String jumlahDenda) {
        this.jumlahDenda = jumlahDenda;
    }
}