public class Pengarang {
    private int IDPengarang;
    private String nama;
    private String alamat;
    private String email;
    private String noTelep;

    public int getIDPengarang() {
        return IDPengarang;
    }

    public void setIDPengarang(int IDPengarang) {
        this.IDPengarang = IDPengarang;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getNoTelep() {
        return noTelep;
    }

    public void setNoTelep(String noTelep) {
        this.noTelep = noTelep;
    }
}